doc.add_paragraph("Нехай M — кількість пікселів у зображенні з дійсними значеннями глибини, θ — параметри моделі. Нехай d = d(θ) ∈ ℝ^M — передбачена диспаратність, а d* ∈ ℝ^M — відповідна правдива диспаратність.")

# Формула (1) - scale- and shift-invariant loss
p = doc.add_paragraph()
p.alignment = WD_ALIGN_PARAGRAPH.CENTER
math2docx.add_math(p, r"\mathcal{L}_{ssi}(\hat{\mathbf{d}}, \hat{\mathbf{d}}^*) = \frac{1}{2M} \sum_{i=1}^{M} \rho(\hat{d}_i - \hat{d}_i^*)")
p_num = doc.add_paragraph()
p_num.alignment = WD_ALIGN_PARAGRAPH.RIGHT
p_num.add_run('(1)')

doc.add_paragraph("де \hat{d} і \hat{d}^* — масштабовані та зсунуті версії передбачень і правдивої глибини, а ρ визначає конкретний тип функції втрат.")

