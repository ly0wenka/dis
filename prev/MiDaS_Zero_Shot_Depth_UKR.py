p_num = doc.add_paragraph()
p_num.alignment = WD_ALIGN_PARAGRAPH.RIGHT
p_num.add_run('(3)')

doc.add_paragraph("де \tilde{d}_i = (d_i, 1)^\top, h = (s, t)^\top")

# --------------------------------------------------------------------------
# Робастне вирівнювання на основі медіани
