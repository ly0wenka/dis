doc.add_paragraph("""
Vision transformer (ViT) працює з представленням зображення як "мішка слів" (bag-of-words). Патчі зображення, окремо вбудовані в простір ознак, або глибокі ознаки, отримані із зображення, виконують роль "слів". Ми називаємо вбудовані "слова" токенами. Трансформери перетворюють набір токенів, використовуючи послідовні блоки багатоголової самоуваги (MHSA), які співвідносять токени один з одним для перетворення представлення.

Важливо для нашого застосування, трансформер підтримує кількість токенів протягом усіх обчислень. Оскільки токени мають взаємно-однозначну відповідність з патчами зображення, це означає, що кодер ViT підтримує просторову роздільність початкового вбудовування протягом усіх трансформерних етапів. Крім того, MHSA є внутрішньо глобальною операцією, оскільки кожен токен може звертатися до будь-якого іншого токена. Отже, трансформер має глобальне рецептивне поле на кожному етапі після початкового вбудовування.
""")

